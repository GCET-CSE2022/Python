## Tuples conatin heterogeneous elements

# Syntax

tuple_1 = (21,'yaseen','cse',4.22)
print(type(tuple_1))
print(tuple_1)

# Another Syntax

tuple_2 = "faheem","cse",21
print(type(tuple_2))
print(tuple_2)

# Accessing tuple

tuple_3 = ('yaseen','numan','faheem',21,20,21)
name = tuple_3[0]
age = tuple_3[3]
print("\nMy name is {} and I'm {}".format(name,age))



